<?php

namespace TroskiShop\Application\DTOs\Products;

use TroskiShop\Domain\Model\Product;

class ProductCatalogDto
{
    private int $id;
    private string $name;
    private float $price;

    /**
     * @param int $id
     * @param string $name
     * @param float $price
     */
    public function __construct(int $id, string $name, float $price)
    {
        $this->id = $id;
        $this->name = $name;
        $this->price = $price;
    }

    public static function createFromProduct(Product $product): self
    {
        return new self($product->getId(), $product->getName(), $product->getPrice());
    }

    public function getId(): int
    {
        return $this->id;
    }

    public function setId(int $id): void
    {
        $this->id = $id;
    }

    public function getName(): string
    {
        return $this->name;
    }

    public function setName(string $name): void
    {
        $this->name = $name;
    }

    public function getPrice(): float
    {
        return $this->price;
    }

    public function setPrice(float $price): void
    {
        $this->price = $price;
    }

}